def build_user_info_prompt(user_info: dict | None) -> str:
    if not user_info:
        return ""
    tech_stacks = user_info.get("techStacks", [])
    tech_stacks_str = ", ".join(tech_stacks) if isinstance(tech_stacks, list) else str(tech_stacks or "없음")
    return f"""

## 현재 사용자 기본 정보
- 이름: {user_info.get('name', '알 수 없음')}
- 이메일: {user_info.get('email', '알 수 없음')}
- 연락처: {user_info.get('phone', '알 수 없음')}
- 역할: {user_info.get('role', '연수생')}
- 기술 스택: {tech_stacks_str}

사용자가 '나', '내', '내가' 등으로 자신에 대해 물어보거나 개인화 추천을 원할 때 위 기본 정보를 적극 참고하세요."""


def build_readiness_summary(readiness: dict | None) -> str:
    if not readiness:
        return "데이터 준비 상태: 확인 전"
    labels = {
        "user_info": "기본정보",
        "user_calendar": "레거시 일정",
        "mentorings": "특강/멘토링",
        "team_info": "팀 정보",
    }
    parts = []
    for key, label in labels.items():
        counts = readiness.get(key, {})
        usable = int(counts.get("valid", 0) or 0) + int(counts.get("partial", 0) or 0)
        total = int(counts.get("total", 0) or 0)
        invalid = int(counts.get("invalid", 0) or 0)
        parts.append(f"{label}: 사용 가능 {usable}/{total}, 제외 {invalid}")
    return "데이터 준비 상태: " + " | ".join(parts)


